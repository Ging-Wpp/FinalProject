    public int[] convertHexToRgb(String hex) {
        return new int[] { Integer.parseInt((hex.substring(1, 3)), 16), Integer.parseInt(hex.substring(3, 5), 16),
                Integer.parseInt(hex.substring(5, 7), 16) };
    }

    public int[] convertHexToHsl(String hex) {
        int[] redgreenblue = convertHexToRgb(hex);
        float rScaled = (float) redgreenblue[0] / 255;
        float gScaled = (float) redgreenblue[1] / 255;
        float bScaled = (float) redgreenblue[2] / 255;
        float minVal = Math.min(rScaled, Math.min(gScaled, bScaled));
        float maxVal = Math.max(rScaled, Math.max(gScaled, bScaled));
        float delta = maxVal - minVal;
        float h, l = (maxVal + minVal) / 2, s;

        if (delta == 0)
            h = 0;
        else if (maxVal == rScaled)
            h = ((gScaled - bScaled) / delta) % 6;
        else if (maxVal == gScaled)
            h = (bScaled - rScaled) / delta + 2;
        else
            h = (rScaled - gScaled) / delta + 4;

        h = Math.round(h * 60);
        if (h < 0)
            h += 360;

        s = delta == 0 ? 0 : delta / (1 - Math.abs(2 * l - 1));
        s = +(s * 100);
        l = +(l * 100);

        if (l > 0 && l < 1)
            s = delta / (l < 0.5 ? (2 * l) : (2 - 2 * l));

        return new int[] {(int) h, (int) s, (int) l};
    }

